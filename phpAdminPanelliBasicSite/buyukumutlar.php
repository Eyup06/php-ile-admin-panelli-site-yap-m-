            karşılaşması ve sonrasında süre gelen olaylar anlatılmakdır. Pip annesi, babası ve kardeşlerini kaybetmiştir bu durum nedeniyle ablası ve onun demirci 
            kocası Joe ile yaşamaktadır. Pip oldukça yaramaz ve laf dinlemeyen bir çocuktur ablasından bu yüzden defalarca kez dayak yemiştir. Ve yine bir gün ablasından
             habersiz kilise mezarlığına ailesinin mezarlarını görmeye gitmiştir ve orada oldukça zaman geçirmiştir. Tam dönmek üzereyken daha doğrusu nehirden karşıya
             geçecekken kaçak bir mahkumla karşılaşır ve oldukça korkar. Mahkum, Pip'in cebinde ne varsa almış ve ona daha fazla yemek ve bir tane eğe getirmesini ister yoksa 
             kalbini yerinden sökeceğini söyleyip Pip'i oldukça korkutur. Pip bu durumdan çok etkilenir ve ablasının yaptığı kıymalı böreği, pudingi Joe'nin aletlerinden ise 
             eğeyi çalıp mahkuma götürmüştür. Mahkum karnını doyurmuş ve ortadan kaybolmuştu fakat Pip olayın şokunu hala üzerinden atamamıştır. Bir gün Pip yine Joe ile 
             konuşurken ablasın börekleri ve pudingi Pip'in çaldığını öğrenmiştir ve uslanıp akıllanması için onu Bayan Havisham'ın yanına oyun oynamaya yollamıştır ve Pip'in
             hayatı o günden sonra değişmiştir.</p>

             <p style="font-family: Georgia, 'Times New Roman', Times, serif;"> Çünkü Pip, Bayan Hevisham'ın üvey kızı Estella'ya aşık olmuştur fakat Estella onun kaba 
