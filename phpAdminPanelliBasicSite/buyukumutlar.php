             darp almış ve konuşma ve hareket etme yetisini kaybetmiştir ve Pip gittikten sonra durumu daha da kötüleşmiş ve hayatını kaybetmiştir, Pip haberi duyar duymaz
              kasabaya dönmüş ve ablasının cenaze törenine katılmıştır, o sırada Bayan Hevisham'a da uğramayı ihlal etmemiş ve Estella'ya artık bir beyefendi olduğunu 
              göstermek istemiştir. Estella,Pip'in bu değişilmesine şaşırmıştır fakat Pip'in beklediği derece değildir ve Pip buna oldukça üzülmüştür. İçinde beslediği 
