             umutlarının yıkılmasına sebep olacak bir adam onu ziyarete gelir. Pip gördüğünde o kadar şaşırır ki o kaçak mahkumu, kovmaktan beter eder. En sonunda her şey
              ortaya çıkar ve aslında miras Pip'e kaçak mahkum tarafından kalmıştır, Bayan Hevisham'ın olayla hiçbir ilgisi yoktur. Pip duydukları karşısında şoka girer ve
