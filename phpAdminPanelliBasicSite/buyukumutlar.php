                zorluklara katlanmıştır, insanların ona söylediği laflara kulak asmamış, her yaptığı işte "Pip için!" diyerek başlamıştır. Pip duydukları karşısında 
                ne diyeceğini bilemez ve umutlarının ikinci kısmı da tam burada sonlanıyor.</p>



