             olarak gördüğü Biddy'e beyefendilik hakkında sorular sormaya başlamıştı. Her şey iyi güzel giderken Pip bir gün ona çok büyük bir mirasın kaldığını öğrenmiş ve 
             Londra'ya yollanmak üzere yola çıkması istenmiştir. Fakat kim tarafından kaldığı belli değildir. Pip bunu Bayan Hevisham tarafından kızı Estella'ya layık bir 
             beyefendi olması için bilerek yapıldığını düşünüp bir mutlulukla ve yeni şık ve oldukça pahalı kıyafetleriyle Londra'ya gitmiştir."Pip'in büyük umutlarının 
             birinci kısmı burada bitiyor."</p>


