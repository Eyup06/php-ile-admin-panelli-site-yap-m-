            kaçak mahkumu yeni ismiyle Provis'i saklamak için elinden geleni yapmaktadır. Ama bununla beraber bazı sırlarda ortaya çıkmadır. Provis'in neden gemi
            hapishanesine girdiğini, neden kaçmak zorunda kaldığını neredeyse her şeyi. Provis,Bay Jaggers'ın evindeki çalışan kadının kocasıdır ve öldüğünü 
            sandıkları küçük kızları Estella'dır. Ve Bayan Hevisham'ı düğün günü terk edip giden nişanlısı Compeyson Provis'in hapise girmesini sağlayan o adamdır.
            Pip,Provis'i yurt dışına kaçırmak için planlar yapmıştır ve onu kayıkla ülkeden kaçıracaklardır fakat Compeyson, Provis'i ihbar etmiş ve kayıkları 
            batırılmıştır. Provis denizden yaralanmış bir şekilde çıkarken elleri ve ayakları kelepçelenmişti ve Compeyson boğularak ölmüştü. Yaşananlar karşısında
            Pip,Provis'ini hiç yalnız bırakmamıştır. Provis'in durumu mahkemeden sonra oldukça kötüleşmiş ve neredeyse son günlerini yaşamaktadır, Pip ona son
            nefesinde kızının yaşadığını ve ona aşık olduğu söylemiştir ve Provis huzur içinde ölmüştür. Pip,Provis'in de gitmesiyle yalnız kalmıştır ve
            borçlarını ödemeyecek hale gelmiştir tam da bu sırada arkadaşı Herbert ona bir iş teklifi sunmuştur fakat Pip hemen kabul etmeyip, kasabaya dönüp 
            Biddy ile evlenmeyi düşünmüştür fakat gittiğinde Biddy'nin Joe ile evlendiğini öğrenir ve arkadaşı Herbert ile Kahire'ye gider. İyi bir iş sahibi olur
            ve hayatı düzene girer ama aklı hala Estella'dadır. Onu görebilme ümidiyle kasabaya Bayan Hevisham'ın eski evine gider ve Estella ile karşılaşır.
            Estella uzun zaman önce evlenmiş ve kötü bir evlilik geçirmiştir ve bu Pip'in hiç istemediği bir evliliktir ama Estella'yı gördüğüne sevinir ve hala
            arkadaş olduklarını birbirlerine söyleyip roman son bulmaktadır.</p>


            <h3 style="font-family: Georgia, 'Times New Roman', Times, serif;" ><mark> Yazar Charles Dickens Hakkında</mark></h3>

            <p style="font-family: Georgia, 'Times New Roman', Times, serif;" > İngiliz yazar ve toplumsal eleştirmen Charles Dickens,7 Şubat 1812 yılında dünyaya gelmiştir. 
            Küçük yaştan buyana büyük zorluklarla karşılaştığından eğitimini tamamlayamamıştır. Bu yüzden çocukluk yılarını çeşitli işlerde çalışarak geçirmiştir. Bir avukatın
            yanında stenografi öğrenen yazar, Morning Chronicle gazetesinde öğrenimi üzerine çalışarak aynı zamanda “Boz’un Karalamaları” adı altında yazılarını yayınlamaya başlamıştır.</p>


            <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >1837 yılında yayınlanan “Bay Pikvik’in Serüvenleri” adlı kitabı ile büyük bir hayran kitlesi 
            oluşturarak edebiyat dünyasına adını duyurmayı başarmıştır. Daha sonra, örf ve âdete yer veren, mizahi kalemini kullanarak yazdığı eserleriyle günden güne
             büyüyerek şöhretini katlamıştır. Hayatını ve çocukluğunu 1850 yılında yazdığı “David Copperfield” adlı romanında anlatmıştır.</p>

             <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Dönemin en ünlü yazarı olarak tarihe adını yazdıran Charles Dickens, Amerika’da Daily News
            ve Household dergilerini çıkararak sık sık seyahatlere çıkıp konferanslarda bulunmuştur. Fakat bu yorucu ritme dayanamayan yazar, evine istirahata çekilerek 
            1870 yılında şöhretinin en doruk noktasında iken hayata gözlerini kapamıştır. </p>


