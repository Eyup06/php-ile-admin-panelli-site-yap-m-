                                         <h3><mark>Roman Yazarı Fyodor Mikhailovich Dostoevsky Hakkında</mark></h3>  
                                         
                                         
                                         <p style="font-family: Georgia, 'Times New Roman', Times, serif;">Rus edebiyatının dünyaca tanınmış yazarlarından olan 
                                            <b>Fyodor Mikhailovich Dostoevsky </b> 1821 yılında Moskova’da doğmuştur. Altı çocuklu bir ailenin ikinci çocuğu olarak 
                                            dünyaya gelmiştir. Babası alkol bağımlısı ve çok sinirli bir insan olduğu için çocuklarını da disiplinli yetiştirmeye
                                             çalışmıştır. Annesi ise etliye sütlüye karışmayan saf bir kadınmış. </p>

                                             <p style="font-family: Georgia, 'Times New Roman', Times, serif;"> <b>Fyodor Mikhailovich Dostoevsky</b> ilköğrenimini 
                                                Moskova’da okumuştur. Kendisi on beş yaşındayken annesini kaybettikten sonra Petersburg Mühendislik Okuluna gitmiştir.
                                                 Buradayken babasının ölüm haberini almıştır. Okulunu bitirdikten sonra asteğmen rütbesiyle Petersburg’daki İstihkam
                                                  Müdürlüğüne verilmiştir. Çok fazla askeri disiplinin olması yüzünden bu işini bırakıp yazarlığa başlamıştır.</p>


                                                  <p style="font-family: Georgia, 'Times New Roman', Times, serif;">Hayatında iki kez evlilik yapan<b> Fyodor Mikhailovich Dostoevsky</b> 
                                                    ayrıca sara hastalığına yakalanmıştır. Bir kanama sonucu 28 Ocak 1881 yılında hayatını kaybetmiştir. O dönemde Rus halkı bu yazar için görülmemiş bir 
                                                    cenaze töreni düzenlemiştir. </p>
                                            

                                                <p style="font-family: Georgia, 'Times New Roman', Times, serif;"> <b>Fyodor Mikhailovich Dostoevsky</b> küçüklüğünden 
                                                    beri yazar olmak istemiştir. Bir yıl askeri görevini yaptıktan sonra işini bırakıp yazar olmaya karar vermiştir. 
                                                    1846 yılında ilk romanı olan İnsancıklar adlı eserini yazmıştır. Dönemin ünlü eleştirmeni olan Belinski’ye romanını 
                                                    göndererek fikir alışverişinde bulunmuştur. Belinski’den tam not alan romanı Rusya’nın ilk toplumsal romanı sayılır.
                                                     Böylece yazar Rus gerçekçi romanında yeni bir okulun kapılarını açmıştır. </p>
                                                    




