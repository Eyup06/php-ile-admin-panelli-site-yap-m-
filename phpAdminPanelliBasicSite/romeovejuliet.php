                          <h3 style="font-family: Georgia, 'Times New Roman', Times, serif;"  > <mark>Romeo ve Juliet'in Özeti</mark></h3>

            <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Olaylar İtalya’nın Verona kentinde geçmektedir. Kentin ileri gelenlerinden olan Montague
                ve Capulet aileleri arasında sebebi açıklanmayan ancak yıllardır sürdüğü anlaşılan bir düşmanlık vardır. İki aile üyeleri karşılaştıkları her yerde kavga
                 etmektedir. Bu kavgaların şehrinin düzenini bozmasından rahatsız olan Verona Prensi Escalus, kavgalardan birine müdahale eder ve eğer kavgalar devam 
                 ederse buna sebep olanların idam edileceğini söyler. </p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Romeo hikâyenin başında Rosaline adlı bir kadına âşıktır. Kuzeni Benvolio, aşkına 
                    karşılık bulamadığı için çok üzgün olan Romeo’ya yardımcı olabilmek için Rosaline’in davet edildiği bir baloya gitmeye karar verir. Balo Capulet 
                    evindedir ancak maskeli bir balo olduğundan ve kendileri de kılık değiştirdiğinden içeriye sorun çıkmadan girerler. </p>



                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" > Juliet ise Paris adlı bir aile dostları ile evlendirilmek üzeredir. Juliet’in babası
                     kızıyla evlenmek isteyen Paris’e son kararın kızına ait olacağını söyleyerek Paris’in baloda kızıyla biraz vakit geçirmesini istemiştir. Juliet’in
                      annesi, babasının bu kararını Juliet’e açıklar ve baloda Paris’i yakından tanımaya çalışmasını söyler.</p>




                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Baloda dans edilirken Romeo maskesini takmış biçimde Rosaline’i aramaktadır. Ancak
                     Rosaline yerine Paris ile dans eden Juliet’i görür ve anında âşık olur. Juliet’in kendisini görmesi için maskesini çıkarınca Juliet de Romeo’ya ilk
                      görüşte âşık olur. </p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Romeo maskesini çıkardığından Tybalt ve Capulet ailesinin diğer üyeleri tarafından
                     fark edilir. Ancak ev sahibi olan Juliet’in babası bir sorun çıkmasını istemediğini belirtir ve ikisi tartışmaya başlar. Bu tartışma üzerine 
                     Paris, Capulet ve Tybalt’ın yanına gitmek için Juliet’in yanından ayrılınca Romeo Juliet’i dışarıya davet eder. Aşkını açıklar ve Juliet’i öper. 
                     Bu sırada Juliet’in dadısı gelir ve Juliet’i Romeo’dan uzaklaştırır. Romeo Dadı’ya kızın kim olduğunu sorunca bir Capulet olduğunu öğrenir.
                      Yine Dadı Juliet’e Romeo’nun düşmanları olan Montague ailesinden biri olduğunu söyler. </p>



                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Bu durum hem Romeo’yu hem Juliet’i üzse de birbirlerini unutamazlar ve Romeo bir kez 
                    daha görebilme ümidiyle Juliet’in evinin yakınlarına gelir. O anda da Romeo’yu aklından çıkaramayan Juliet odasının balkonuna çıkınca karşılaşırlar ve
                     Romeo aşkını itiraf ederek Juliet ile evlenmek istediğini söyler. Juliet de bu teklifi kabul eder ancak ailelerin arasındaki düşmanlıktan ötürü bunu 
                     gizlice yapmaya karar verirler. </p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Romeo, kendilerini evlendirmesi için Rahip Lawrence’den yardım ister. Rahip ilk başta
                     ailelerinden habersiz böylesi bir evliliği yapmak istemez ancak bu evliliğin aileler arasındaki düşmanlığı bitireceğini düşündüğünden kabul eder.
                      Dadı’nın da yardımıyla Romeo ve Juliet evlenirler. </p>



                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Bu esnada Tybalt, akşam baloya davetsiz gelmesinin kendilerini aşağılamak için 
                    yapıldığını düşündüğünden düelloya davet etmek için Romeo’yu aramaktadır. Bu esnada Mercutio ve Benvolio ile karşılaşır. İkisiyle konuşurken Romeo 
                    ortaya çıkar. Romeo artık Tybalt ile akraba olduğundan tartışma ya da düello yapmak istemez ancak Tybalt aşağılayıcı konuşmalar yapınca Mercutio 
                    sinirlenerek dövüşe başlar. Romeo kimsenin ölmesini istememektedir bu yüzden araya girer ve Mercutio’yu tutarken kendisini savunamayan arkadaşı 
                    Tybalt tarafından öldürülür. Bunun üzerine Romeo kendini kaybeder Juliet’i ve evliliği unutarak Tybalt ile kılıç dövüşüne başlar ve Tybalt’ı öldürür.
                     Kendine geldiğinde yaptığından pişman olur ancak Verona Prensi kendisini sürgünle cezalandırır. Mantua kentine sürgüne gönderilir. </p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" > Yeğeni Tybalt’ın kendi sözünü dinlemediği için öldüğünü gören Yaşlı Capulet artık 
                    tercihleri gençlere bırakmamaya karar verir ve Juliet’in Paris ile evleneceğini söyler. Juliet istemediğini söylese de babası evlatlıktan reddeceği
                     tehdidinde bulunur. Juliet yardım etmesi için Rahip Lawrence’e gider. Rahibin yardım etmek istemediğini görünce kendini bıçaklayarak öldürmek ister.
                      Rahip Lawrence başka çaresi kalmadığını görünce Juliet’e bitkilerden bir ilaç yapar. Bunu içtiğinde nabzı duracak ve herkes öldüğünü düşünecektir.
                       Aile kabrine konulduktan sonra Romeo ile birlikte geleceklerini ve kendisini alarak kaçacaklarını söyler. Juliet akşam odasında ilacı içer ve sabah 
                       olunca herkes tarafından öldüğü düşünülür.</p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Juliet’in bu ilacı içeceğinden Romeo’nun haberi yoktur. Rahip Lawrence, sürgünde olan
                     Romeo’ya planlarıyla ilgili bir mektup yazar. Ancak bu mektup Romeo’ya ulaşmadan önce bir yardımcısı Romeo’nun yanına gelerek Juliet’in öldüğünü 
                     söyleyince Romeo evi terk eder ve mektup da böylece kendisine ulaşamaz.</p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >O gece Rahip Lawrence mezarlıkta Romeo’yu beklemektedir. Çünkü mektubu almadığını 
                    bilmemektedir. Bu esnada mektubu vermesi ile görevlendirdiği Rahip John yanına gelerek Romeo’yu bulamadığını söyler. Bunun üzerine Rahip Lawrence, 
                    hızla Juliet’in kabrine yönlenir ancak geç kalır. </p>

                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" > Aynı anda Paris de Juliet’in mezarını ziyarete gelmiştir. Mezarlıktayken elinde bir
                     levye ile gelen Romeo’yu görür. Kabrin kapısını açmaya çalışan Romeo’ya müdahale edince Romeo kafasına levye ile vurarak Paris’i öldürür.</p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" >Romeo kabrin içine girince Juliet’i görür. Sadece uyuduğundan haberi olmadığından 
                    yanında getirdiği zehri içerek ölür. Hemen ardından Juliet uyanır ve Romeo’nun öldüğünü görünce kalbine bıçak saplayarak intihar eder. </p>


                 <p style="font-family: Georgia, 'Times New Roman', Times, serif;" > Capulet ve Montague ailesi birlikte mezara gelince çocuklarının ölüleriyle 
                karşılaşırlar. Rahip Lawrence olayları anlatınca kavgalarından pişman olarak barışırlar.</p>


                <h3><mark>Yazar William Shakespeare hakkında</mark></h3>


                <p style="font-family: Georgia, 'Times New Roman', Times, serif;">Dünyaca ünlü İngiliz yazar<b>  William Shakespeare  </b> Nisan 1564 yılında İngiltere’de doğmuştur.
                     John Shakespaere’nin üç oğlundan biridir. Babası tüccar olduğu için maddi durumu iyidir. Bu nedenle eğitim dili Latince olan bir okulda eğitim görmüştür.
                      Bu okulda Roma edebiyatı klasikleri eğitimi aldığı için üniversiteye gitmemiştir. William Shakespeare 18 yaşındayken kendisinden 8 yaş büyük olan
                       Anne Hathaway ile evlenmiştir. Bu evlilikten 3 çocuğu dünyaya gelmiştir. Yazdığı eserler sayesinde hatırı sayılır gelir elde etmiştir.
                        Gelirlerini emlak üzerinden değerlendirmiştir. Böylece emlak zenginleri arasına girmeyi başarmıştır. </p><br>


                        <p style="font-family: Georgia, 'Times New Roman', Times, serif;" ><b> William Shakespeare</b> dünyada gelmiş geçmiş en iyi oyun yazarı olarak bilinir.
                             Venns and Adonis ile The Rape of Lucrece’de büyük ustalığını ve zengin düş gücünü koymuştur. 1609 yılında yayımladığı The Sonnets Türkçe 
                             adıyla Soneler ya da Tüm Soneler ile ünlü bir yazar olmuştur. İçinde 154 sonesi bulunan bu eserinde Elizabeth dönemi sone dizelerinde görülen
                              anlatının dışında bir anlatımla yazmıştır. Sonelerinin bir bölümü çok açıkken, bir başka bölümü ise anlaşılmayacak kadar kapalıdır. </p><br>

                              <p style="font-family: Georgia, 'Times New Roman', Times, serif;"> Şiirlerinde normal insan yaşantısına yakın sakin bir hava yaratmıştır. 
                                Düş kırıklığı, ayrılık, endişe, yabancılaşma ve başarısızlık gibi duyguları işlemiştir. 

                                William Shakespeare’in sonelerinin gizi günümüze kadar çözülememiştir. Bu nedenle tam olarak anlaşılamamıştır. Ancak bu soneler en çok 
                                ilgi gören yapıtlar arasında da yer almıştır.<b> William Shakespeare </b> sadece Elizabeth döneminin değil tüm dünyanın gelmiş geçmiş 
                                en iyi oyun yazarı olarak bilinir. Oyunları çeşitli dillere çevrilmiş ve dünyanın çeşitli yerlerinde sahnelenmiştir.Oyunlarında insan 
                                doğasındaki her türlü duyguyu ve çatışmayı hafızalara kazınacak sözcük ve imgelerle yansıtmıştır. İlk oyunları tarihsel oyun, komedi, 
                                intikam ve romantik trajedi türlerindendir. Oyun konularının çoğunu İngiliz tarihinden almıştır. İlk tarihsel oyununu derinliği olmayan 
                                karakterlerden seçmiştir. Daha sonraki oyunlarında ise derinliği olan karakterler seçmiştir. </p><br>

                                <p style="font-family: Georgia, 'Times New Roman', Times, serif;"> William Shakespeare büyük bir ironi ustasıdır. İnsan davranışlarını 
                                    dramatik bir yöntemle sorgulamıştır. Çeşitli etkileşimler ve bunların doğurduğu sonuçları ortaya koymak onun için amaçtı. Komedilerini 
                                    aşk ve evlilik gibi insan yaşamının özel yanlarına ilişkin yazmıştır. Bu oyunlarında büyü ögeleri ve mucizevi dönüşümler vardır. Ayrıca
                                     cinsiyeti gizlenen karakterler ve birbirine tıpatıp benzeyen ikizler de yer almaktadır. Beklenmedik sahne dışı gelişmelerle olay akışını
                                      tersine çevirmiştir. Komedi oyunlarının içinde birçok öykü iç içe geçmiştir. Genellikle evlilikle son bulan bu komediler izleyicinin 
                                      beklentisini karşılamaktadır. Düşünsel bütün olarak yazılan komedileri bir dans ya da satranç oyunu gibi zarif ve karmaşıktır. </p><br>


                                      <p style="font-family: Georgia, 'Times New Roman', Times, serif;">William Shakespeare birçok oyununun öyküsünü kendi yaratmamıştır.
                                         Onun özgünlüğü kaynak olarak aldığı ögelerin tiyatroyla uyumsuz olan yönlerini ayıklamasından ve kısaca değinilen tiplerin inandırıcılığını 
                                         artırarak geliştirmesinden kaynaklanmaktadır. Oyunlarında ayrıca yeni karakterler yaratmış ve kaynak olarak aldığı ögelerin kurgusunu değiştirmiştir.
                                          Diyalogları oldukça yoğundur ve oyunları geniş felsefi yaklaşımlarla yazılmıştır. William Shakespeare oyunlarını sahnelenmek için yazmıştır.  Sözcük ve 
                                          imgeleri, ses, ritim ve ölçü kullanımı, satır vurgusu, düzeni ve lirikliği birçok okuyucunun büyülenmesini sağlamıştır.<b> William Shakespeare </b>
                                           yetersiz eğitim görmesine rağmen yazdığı yapıtlar sayesinde büyük bir edebiyatçı olduğunu kanıtlamıştır. Bu sayede dünyanın en iyi edebiyatçıları arasında 
                                           yerini almıştır.<b> William Shakespeare </b> son günlerini Stratford’da geçirmiştir. İngiltere’nin ulusal şairi ve Avon’un Ozanı olarak
                                            anılan ünlü şair 23 Nisan 1616 yılında Stratford’da vefat etmiştir. </p><br>


                  
