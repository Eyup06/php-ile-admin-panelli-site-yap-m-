        <h3 style = "font-family:courier,arial,helvetica;"><mark> Sefiller Özet  </mark></h3>
         



<strong><p style = "font-family:courier,arial,helvetica;">Sefiller romanı beş cilt ve her biri bir roman büyüklüğünde beş kitaptan oluşuyor.</p>

<p style="font-family:courier,arial,helvetica;">Sefiller, Jan Valjean (Jan Valjan) ve polis müfettişi Javert (Javer) arasında sürüp giden bir kovalamacanın, 
kız kardeşinin çocukları için ekmek çaldığı için beş yıla mahkum edilen Jan Valjean’ın öyküsüdür. </p>


<p style="font-family:courier,arial,helvetica;">Jan Valjean, -yalnızca- bir somun ekmekten dolaya kürek cezasına çarptırılır, birkaç kez kaçma girişimiyle cezası 
on dokuz yıla çıkar. 1815’te serbest kalır, ama hapisten sonra inançlarını yiritirmiş, topluma öfke ve kin duymaktadır. Sefil bir halde "D" kasabasına gider, 
burada kasabanın piskoposundan gördügü iyilikle ruhu aydınlanır. Çünkü Piskopos ona çok iyi davranır. Jan Valjean yıllar sonra böylesine güzel bir davranış 
karşısında çok mutlu olur. Ne var ki Jan Valjean bu güzel davranışı görmez, Piskoposun gümüş yemek takımlarını çalmakla karşılık verir. Ancak yakalandığında polis, 
yüzleştirmek için Piskoposun karşısına getirdiğinde ise, Piskopos yine güzel davranışın bir başka seçkin örneğini verecek, yemek takımlarını kendisinin hediye 
ettiğini söyleyecek, dahası ona "Şamdanları unutmuşsun" diyecektir.</p>




<p style="font-family:courier,arial,helvetica;">Jan Valjean, bu kez Piskopostan özür dileyip artık hayata dürüst ve erdem sahibi iyiliksever bir insan olarak yeniden 
başlayacağına söz verir. Fransa’nın kuzeyinde ucuz mücevher imalatçılığı yaparak yaşamaya başlar. Geçmişini gizler, kısa sürede zengin olur ve herkesin sevgisini 
kazanarak, kasabaya belediye başkanı seçilir.</p>


<p style="font-family:courier,arial,helvetica;"> Ne var ki Jan Valjean’ın geçmişini gizlemesi kuşkulan üzerine çeker. Polis Javert, bir hırsızlık olayından yola çıkarak,
araştırmalara başlar. Konu "D" kasabasındaki hırsızlık olayına kadar gelir. Jan Valiean’ı tutuklayacaktır ki, aynı adı taşıvan bir başkası tutuklanır. Bunun üzerine 
polis Javert, Jan Valjean’dan özür diler, böyle bir yanlışlık karşısında istifa etmek ister, ancak istifası kabul edilmez. Konu kapanır. Ne var ki Valjean’ın ahlâkı, 
kendi yerine bir başkasının hapsedilmesine izin vermez. Mahkemeye gider, gerçeği anlatarak, suçlu olduğunu söyler. Teslim olur ve hapisaneye girer. Bir gece yattıktan 
sonra kaçar.</p>



<p style="font-family:courier,arial,helvetica;"> Jan Valjean, teslim olmadan önce sakladığı -hak ederek kazanılmış- paralanın alır, eski bir fahişe olan Fantina’nın ölümünden sonra üvey anne babası
tarafindan kötü yetiştirilen, Fantina’nın kızı Cosette’i (Kozet) bulur ve onu da yanına alarak bir manastıra sığınır. Manastırın bahçıvan olarak çalışmaya başlar.
Evlat edindiği Cosette ise, rahibe okuluna gider. Sanki polis müfettişi Javert’den kurtulmuş gibidir. Güvenlik içinde yaşar. </p>


<p style="font-family:courier,arial,helvetica;"> Yıllar böyle sakin geçerken, Cosette’in genç ve güzel bir genç kız olmasıyla değişir. Cosette, babası Napolyon ordusunda
subaylık yapmış birinin oğlu olan öğrenci Marius’a (Maryüs) âşık olur. Zengin dedesi tarafindan büyütülen Marius, her zaman haklıdan yana olan Jan Valjean gibi, 1832’de
isyan eden sosyalistlerin safındadır. </p>



<p style=" font-family:courier,arial,helvetica;"> Cosette ile Marius, Paris’in Luxemburg Gardens adındaki parkında tanışırlar ve Valjean’ın kendisini gizli tutmasına
rağmen, gizli gizli mektuplaşırlar. Paris ayaklanmıştır. Sosyalistler, 1832’de, Paris’te hanedânlığa karşı başarısız kalan bir başkaldırma hareketine başvururlar. 
Marius ve arkadaşları, bu başkaldırıya katılırlar. Jan Valjean da onların içindedir. Paris kanla yıkanırken, Javert ile Jan Valjean yine karşı karşıya gelirler.
Jan Valjean, Javert’i ve Marius’ü ölümden kurtarır. Javert, Valjean’ı tutuklamaz. Valjean da fırsattan yararlanıp Javert’i öldürmeye kalkmaz. Ancak bu soylu 
davranış karşısında bütün inandığı değerlerin sarsıldığını gören Javert, hayatında ilk kez olarak, bir mahkumun, kanuna saygı duyan bir vatandaştan daha iyi bir 
insan olacağına inanır. Bir polis memuru olarak bugüne kadar sahte olasılıklara göre işleri yürüttüğü inancına varır. Valjean’ı tutuklama yerine kendini öldürür.
Başkaldıranların durumu parlak değildir. Marius ağır yaralanır ve Valjean onu yeraltındaki kanalizasyonlardan kaçırarak kurtarır. Büyükbabasının evine getirir. 
Kendisini kimin kurtardığını bilmemektedir. </p>



<p style="font-family:courier,arial,helvetica;"> Cosette’in bu genci sevdiğini anlayan Valjean, onların arasına girmemeye karar verir. Cosette’nin Marius’u sevdiğini, onunla evlenmek istediğini anlar. 
Cosette’ye büyük miktarda para verir. Şimdi bir barones olan Cosette’nin eski bir kürek mahkumunun kızı olarak kendisinin bilinmesini istemez ve yalnız yaşamak ister.
Oysa Marius, hayatını kurtaran kişinin Valjean olduğunu öğrenmiştir. İki genç, ölüm yatağında son anlarını yaşayan Valjean’a koşarlar. Yatağında onları karşılar.
Bu karşılaşmada duygusal anlar yaşanır. Her üçü de gözyaşlarını tutamaz. Ölüm yatağında bile onların mutlu olmasını dile getirir. Yıllarca önce piskoposun büyük 
bir erdemli davranışla kendisine hediye ettiği ve böylece ruhunu kazandığı gümüş şamdanları Cosette’e verir.Cosette ve Marius evlenirler. Zaman içerisinde iyice yaşlanan
Jan Valjan da yatağa düşer bir süre sonra da ölür. </p></strong>
    


<h3 style="font-family:courier,arial,helvetica;"> <mark>Roman Yazarı Victor Hugo Hakkında Bilgi </mark></h3>


<strong><p style="font-family:courier,arial,helvetica;"> Büyük Fransız Şair ve yazarı <b>Victor Hugo</b>, Fransa tarihinin en çalkantılı günlerinde, 1802'de geldi dünyaya.
 Babası, Napolyon ordusunda generaldi imparatorun parlak döneminde önemli görevlerde bulundu, bir çok dış ülkeye seyahat etti ve Madrit'te valilik yaptı. 
Anne ve babası arasındaki bitmek bilmeyen geçimsizlikler, yinelenen ayrılıklar nedeniyle, Hugo genellikle annesinden uzak kaldı ve babası ile yaşadı. </p>


<p style="font-family:courier,arial,helvetica;"> İlkokula da İspanya'da başladı. Ancak, İspanyol aristokratlarının çocuklarını kabul eden bu okulda,
 sonradan soyluluk unvanı almış bir burjuva generalin oğlu olması, alay konusu edilerek dışlanmasına yol açtı. Yazarların ürünleri ile yaşam öyküleri arasında 
ilişki kurmak eğilimindeki araştırmacılar, İspanyol okulunda geçen günlerin, Hugo'nun aristokrasiye bir yandan hayranlık duyup bir yandan da nefret etmesi gibi
gerilimli bir duyguya kapılarak liberal-demokratik ilkeleri seçmesinde büyük rol oynadığını iddia etmişlerdir. </p>


<p style="font-family:courier,arial,helvetica;">Napolyon'un imparatorluktan düşmesi ile birlikte Hugo ailesi için zor günler başladı.Babası Paris'e döndü.
Maddi sıkıntılar ve toplumsal çalkantılar içerisinde, eğitimini düzgün bir biçimde sürdüremedi Hugo, ama kendi kendine okumayı sürdürdü, hatta ilk şiirlerini
yazması da bu yıllara denk düşer. Annesinin ölümüyle sefaletin eşiğine gelen Hugo'yu bu güç durumdan kurtaran yirmili yaşlarda yayınlanan -kraliyet yanlısı-
şiirleri oldu; XVIII.Lois tarafından aylığa bağlandı, Chateaubriand'ın ilgisini çekti ve romantik akımı benimsemesinden sonra parlak bir kariyerin kapısını
araladı.</p>


<p style="font-family:courier,arial,helvetica;">1827'de "Cromwell" ve 1830'da "Hernani" oyunları, -tıpkı Namık Kemal'in <b>Vatan Yahut Silistre</b>'sinin Osmanlıda 
yarattığı- isyana benzer bir heyecan uyandırdı Paris'te. Hugo'nun ilk romanı ise "Notre Dame'ın Kamburu"dur(1831). Bugün okunduğunda, yazarın en yüzeysel ürünü olarak 
değerlendirebileceğimiz bu romanın nispi başarısızlığı, Hugo'nun maddi nedenlerle yayınevinin ısrarına boyun eğerek metnini çok kısa bir sürede tamamlamak zorunda 
kalmasındandır. Yine de, Hugo'nun yükselen ünü, bu kitabının da sevilerek okunmasını sağlamıştır Fransa'da.</p>


<p style="font-family:courier,arial,helvetica;">1831-1941 arasında çok sayıda şiir, piyes ve roman yazdı Hugo, 1841'de Fransız Akademisine seçildi. 1848 ihtilalinden
sonra Cumhuriyetçi saflara geçti ve Cumhurbaşkanlığı için aday bile oldu. Kendisi seçilemedi, ama seçilen Louis Napolyon'u destekledi. Ancak bu Napolyon da 
imparatorluğunu ilan edince, Hugo 1851'de Fransa topraklarını terk ederek -yirmi yıl sürecek gönüllü bir sürgünü geçireceği- Channel Adaları'na yerleşti.
Burada yazdığı "Sefiller"(1862), onun en çok tanınan ve sevilen eseridir. İmparatorluk dönemi sona erip Üçüncü Cumhuriyet kurulunca, Victor Hugo, Paris'e bir 
kahraman olarak döndü. Millet meclisine seçildi, ama politikadan çok edebiyatla ilgilenmeyi tercih etti. Victor Hugo, 1885'te öldüğünde, büyük bir törenle 
Pantheon'a gömüldü.  </p> </strong>
         



  









